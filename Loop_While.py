# While Loop
a = 0
while a <= 5:
    print("Value is "+str(a))
    a = a + 1

# For Loop
for x in range(1, 10):
    print(x)

for y in range(9):
    print(y)

# For Loop with Break Statment
for z in range(1, 20):
    if z == 15:
        print(z)
        break


# For Loop with Continue statement to escape the iteration
for q in range(1, 10):
    if q == 5:
        continue
    print(q)





